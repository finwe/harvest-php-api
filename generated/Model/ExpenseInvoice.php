namespace JoliCode\Harvest\Api\Model;

class ExpenseInvoice
{
    /**
     * @var int|null
     */
    protected $id;
    /**
     * @var string|null
     */
    protected $number;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function setId(?int $id): self
    {
        $this->id = $id;

        return $this;
    }

    public function getNumber(): ?string
    {
        return $this->number;
    }

    public function setNumber(?string $number): self
    {
        $this->number = $number;

        return $this;
    }
}
